import UIKit

extension UIColor {
    class var sectionBg: UIColor {
        return UIColor(named: "section_bg")!
    }
    
    class var sectionHeaderLabel: UIColor {
        return UIColor(named: "section_header_label")!
    }
    
    class var tagColor: UIColor {
        return UIColor(named: "tag_color")!
    }
    
    class var emptyTextLabel: UIColor {
        return UIColor(named: "empty_text_label")!
    }
    
    class var textLabel: UIColor {
        return UIColor(named: "text_label")!
    }
    
    class var highlight: UIColor {
        return UIColor(named: "highlight")!
    }
    
    class var settingBlack: UIColor {
        return UIColor(named: "setting_black")!
    }
}
